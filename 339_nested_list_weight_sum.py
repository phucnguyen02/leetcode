class Solution:
    def dfs(self, nested_list, depth):
        sum = 0
        for elem in nested_list:
            if not elem.isInteger():
                sum += self.dfs(elem.getList(), depth + 1)
            else:
                sum += elem.getInteger() * depth
        return sum
    
    def depthSum(self, nestedList) -> int:
        return self.dfs(nestedList, 1)
    
